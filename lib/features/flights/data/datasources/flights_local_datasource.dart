  Future<List<FlightModel>> getByRoute(String fromCode, String toCode) async {
    await Future.delayed(const Duration(milliseconds: 600));
    var flights = _buildFlights();
    if (fromCode.isNotEmpty) {
      flights = flights.where((f) => f.fromCode == fromCode).toList();
    }
    if (toCode.isNotEmpty) {
      flights = flights.where((f) => f.toCode == toCode).toList();
    }
    return flights;
  }

  Future<List<FlightModel>> getDomesticFlights() async {
    await Future.delayed(const Duration(milliseconds: 600));
    return _buildFlights()
        .where(
          (f) =>
              _domesticCodes.contains(f.fromCode) &&
              _domesticCodes.contains(f.toCode),
        )
        .toList();
  }

  Future<List<FlightModel>> getInternationalFlights() async {
    await Future.delayed(const Duration(milliseconds: 600));
    return _buildFlights()
        .where((f) => !_domesticCodes.contains(f.toCode))
        .toList();
  }
